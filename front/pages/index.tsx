import { Container, Text, Heading } from "@chakra-ui/react";
import type { GetStaticProps } from "next";
import Head from "next/head";
import React from "react";
import { NavBar } from "../components/common/navbar";
import { getLastArticle } from "../utils/fs";
import { Article } from "../types/article";
import { MarkDownBuilder } from "../components/common/markdownBuilder";
import { TitleWithTags } from "../components/articles/titleWithTags";
import { TAG_COLOR } from "../const";

const Home = ({ article }: { article: Article }) => {
  const tags = article.tags.map((t) => ({
    name: t.name,
    key: t.id,
    size: "lg",
    color: TAG_COLOR[t.name as keyof typeof TAG_COLOR],
  }));
  return (
    <>
      <Head>
        <title>{article.title} - Hut K</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <NavBar>
        <Container maxW={"80%"} py={5} centerContent={true} pl={10}>
          <TitleWithTags
            title={article.title}
            tags={tags}
            date={article.created_at.slice(0, 10)}
          />
        </Container>
        <MarkDownBuilder markdown={article.content} />
      </NavBar>
    </>
  );
};
export default Home;

export const getStaticProps: GetStaticProps = async () => {
  const article = await getLastArticle("content");
  return {
    props: {
      article,
    },
  };
};
